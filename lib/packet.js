function indexOf(source, search) {
  const len1 = source.length;
  const len2 = search.length;
  if (!len1 || !len2) return -1;
  for (let i = 0, counter = 0; i < len1; ++i) {
    if (source[i] !== search[counter]) {
      counter = 0;
    }
    else if (++counter >= len2) {
      return 1 + i - counter;
    }
  }
  return -1;
}

